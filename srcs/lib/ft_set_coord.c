#include <fdf.h>

t_coord		ft_set_coord(int x1, int y1, int x2, int y2)
{
	t_coord	c;

	c.x1 = x1;
	c.y1 = y1;
	c.x2 = x2;
	c.y2 = y2;
	return (c);
}
