#include <fdf.h>

void		ft_error(char *file, char *func, int line, char *msg)
{
	ft_printf("{red}ERROR:\n\t{yellow}%s: %s(line %d){eoc}\n\t",
			file, func, line);
	if (msg == NULL)
		perror(NULL);
	else
		ft_printf("%s\n", msg);
	exit(EXIT_FAILURE);
}
