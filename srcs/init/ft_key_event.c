#include <fdf.h>

int			ft_key_event(int keycode, t_a *a)
{
	static int	x = 100;
	static int	y = 100;
	int			i;

	ft_printf("{green}(key %d){eoc}\n", keycode);
	i = 0;
	while (++i < 10)
		ft_put_px(a, ++x, ++y, 0xFF0000);
	ft_print_win(a);
	return (0);
}
