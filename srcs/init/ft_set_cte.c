#include <fdf.h>

void		ft_set_cte(t_a *a)
{
	a->para_cte = (PARA_CTE / pow(a->max_z, 2) * a->zoom) * a->mult_cte;
	a->isom_cte1 = (ISOM_CTE1 / pow(a->max_z, 2) * a->zoom) * a->mult_cte / 100;
	a->isom_cte2 = (ISOM_CTE2 / pow(a->max_z, 2) * a->zoom) * a->mult_cte / 100;
}
